class atom:

    def __init__(self, z):

        self.z = z
        self.name = elements[z]
        self.abreviation = abreviations[z]
        self.molarmass = masses[z]
        self.oxinums = oxidation_numbers[z]
        self.elecconfig = elecconfig[z]

        #Getting the element type
